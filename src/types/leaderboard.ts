/** View-модель одного рядка лідерборду. */
export interface Leader {
  /** ID гравця (для порівняння з поточним користувачем — підсвітка свого рядка). */
  id: string
  /** Позиція у списку (1..N). */
  rank: number
  /** Нік з префіксом, напр. "@User_name". */
  nickname: string
  /** Кількість перемог. */
  wins: number
}
